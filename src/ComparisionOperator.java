public class ComparisionOperator {
    public static void main(String [] args){

        //comparision op
        //== => =< != > <

        //== -- assignment operator
        // == -- equal operator

        int x = 10;
        int y = 20;
        int a = 15;
        int b = 25;

        System.out.println(x == y);
        System.out.println(x<y);
        System.out.println(x>y);
        System.out.println(x>=y);
        System.out.println(x<=y);
        System.out.println(a>b);
        System.out.println(b==y);
        System.out.println(b>=y);
        System.out.println(x!=y);
    }
}
